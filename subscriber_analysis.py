#DATA PREPROCESSING

data.head()

#data=data.drop(["customerID"],axis=1)

# 1. 'TotalCharges' ko numeric mein convert karein
data['TotalCharges'] = pd.to_numeric(data['TotalCharges'], errors='coerce')

# 2. Check karein ki kitne null values create huye
print(data['TotalCharges'].isnull().sum())

# 3. Chote amount mein nulls hain toh unhe drop kar dete hain
data.dropna(subset=['TotalCharges'], inplace=True)

# 4. Verify karein ki type change hua ya nahi
print(data['TotalCharges'].dtype)

cat_data=data.select_dtypes(include=["object"]).columns

#all columns
cat_data









# encoding
data= pd.get_dummies(data,columns=cat_data,drop_first=True,dtype=int)

data.head()

x=data.drop("Churn_Yes",axis=1)
y=data["Churn_Yes"]

from sklearn.model_selection import train_test_split
x_train,x_test,y_train,y_test=train_test_split(x,y,test_size=0.2,random_state=42)

from sklearn.preprocessing import StandardScaler

# 1. Scaler ka object banayein
scaler = StandardScaler()

# 2. X_train par 'fit' aur 'transform' dono karein
x_train_scaled = scaler.fit_transform(x_train)

# 3. X_test par sirf 'transform' karein (Fit nahi karna hai)
x_test_scaled = scaler.transform(x_test)

from sklearn.linear_model import LogisticRegression
model=LogisticRegression()
model.fit(x_train_scaled,y_train)
y_predict=model.predict(x_test_scaled)

from sklearn.metrics import accuracy_score, classification_report, confusion_matrix
# Accuracy score check karein
accuracy = accuracy_score(y_test, y_predict)
print(f"Model Accuracy: {accuracy * 100:.2f}%")

# Detailed Report (Precision, Recall, F1-score)
print("\nClassification Report:")
print(classification_report(y_test, y_predict))

cm=confusion_matrix(y_test,y_predict)
sns.heatmap(cm,annot=True,fmt="d")
plt.xlabel("predicted")
plt.ylabel("actual")
plt.title("confusion matrix")
plt.show()

cm=confusion_matrix(y_test,y_predict)
print(cm)









# Agar XGBoost try karna hai toh:
from xgboost import XGBClassifier

xgb_model = XGBClassifier()
xgb_model.fit(x_train_scaled,y_train)
xgb_pred = xgb_model.predict(x_test_scaled)

print(confusion_matrix(y_test, xgb_pred))

# 'scale_pos_weight' calculate karein (Ratio of No to Yes)
# Agar aapka data imbalanced hai, toh ye formula use karein:
ratio = float(y_train.value_counts()[0]) / y_train.value_counts()[1]

xgb_model_tuned = XGBClassifier(
    n_estimators=100,
    max_depth=5,
    learning_rate=0.1,
    scale_pos_weight=ratio, # Ye sabse important hai balance ke liye
    random_state=42
)

xgb_model_tuned.fit(x_train_scaled, y_train)
xgb_pred_tuned = xgb_model_tuned.predict(x_test_scaled)

from sklearn.metrics import confusion_matrix
print(confusion_matrix(y_test, xgb_pred_tuned))

import matplotlib.pyplot as plt
from xgboost import plot_importance

# Feature importance plot karein
plot_importance(xgb_model_tuned, max_num_features=10)
plt.show()

import matplotlib.pyplot as plt
import pandas as pd

# XGBoost ke feature importances nikalna
feature_importances = pd.Series(xgb_model_tuned.feature_importances_, index=x.columns)

# Top 10 features ko plot karna
plt.figure(figsize=(10, 6))
feature_importances.nlargest(10).sort_values().plot(kind='barh', color='teal')
plt.title("Top 10 Reasons for Customer Churn (XGBoost)")
plt.xlabel("Importance Score")
plt.ylabel("Features")
plt.show()

